
CREATE DATABASE consultorias_db;
USE consultorias_db;

CREATE TABLE clientes (
    id_cliente INT AUTO_INCREMENT,
    nome VARCHAR(150) NOT NULL,
    email VARCHAR(150) UNIQUE NOT NULL,
    empresa VARCHAR(150),
    telefone VARCHAR(20),
    data_cadastro DATE,
    PRIMARY KEY (id_cliente)
);

CREATE TABLE projetos (
    id_projeto INT AUTO_INCREMENT PRIMARY KEY,
    id_cliente INT NOT NULL,
    nome_projeto VARCHAR(200) NOT NULL,
    descricao TEXT,
    data_inicio DATE,
    data_fim DATE,
    status ENUM('planejamento','em_andamento','concluido','cancelado') DEFAULT 'planejamento',
    FOREIGN KEY (id_cliente) REFERENCES clientes(id_cliente)
);

CREATE TABLE analistas (
    id_analista INT AUTO_INCREMENT PRIMARY KEY,
    nome VARCHAR(150) NOT NULL,
    especialidade ENUM('R','Python','SQL','PowerBI','Estatística','Machine Learning') NOT NULL,
    senioridade ENUM('Junior','Pleno','Senior') NOT NULL
);

CREATE TABLE atividades (
    id_atividade INT AUTO_INCREMENT PRIMARY KEY,
    id_projeto INT NOT NULL,
    id_analista INT NOT NULL,
    titulo VARCHAR(200) NOT NULL,
    descricao TEXT,
    data_atribuicao DATE NOT NULL,
    data_conclusao DATE,
    status ENUM('pendente','em_execucao','concluida') DEFAULT 'pendente',
    FOREIGN KEY (id_projeto) REFERENCES projetos(id_projeto),
    FOREIGN KEY (id_analista) REFERENCES analistas(id_analista)
);

CREATE TABLE entregas (
    id_entrega INT AUTO_INCREMENT PRIMARY KEY,
    id_atividade INT NOT NULL,
    tipo_entrega ENUM('Relatório','Dashboard','Script','Banco de Dados','Apresentação') NOT NULL,
    link_arquivo VARCHAR(300),
    data_entrega DATE NOT NULL,
    FOREIGN KEY (id_atividade) REFERENCES atividades(id_atividade)
);

CREATE TABLE faturas (
    id_fatura INT AUTO_INCREMENT PRIMARY KEY,
    id_cliente INT NOT NULL,
    valor DECIMAL(10,2) NOT NULL,
    data_emissao DATE NOT NULL,
    data_vencimento DATE NOT NULL,
    status ENUM('pendente','pago','atrasado') DEFAULT 'pendente',
    FOREIGN KEY (id_cliente) REFERENCES clientes(id_cliente)
);

CREATE TABLE pagamentos (
    id_pagamento INT AUTO_INCREMENT PRIMARY KEY,
    id_fatura INT NOT NULL,
    valor_pago DECIMAL(10,2) NOT NULL,
    data_pagamento DATE NOT NULL,
    metodo ENUM('PIX','Cartão','Boleto','Transferência') NOT NULL,
    FOREIGN KEY (id_fatura) REFERENCES faturas(id_fatura)
);

CREATE TABLE logs_sistema (
    id_log INT AUTO_INCREMENT PRIMARY KEY,
    id_analista INT,
    acao VARCHAR(300) NOT NULL,
    data_evento DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (id_analista) REFERENCES analistas(id_analista)
);

